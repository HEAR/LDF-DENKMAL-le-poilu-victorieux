			<p>"La colonne représente l'axe qui donne vie et immortalité à l'édifice qu'elle soutient et à tout ce qu'il représente. Dans la tradition celtique, elle est le symbole de l'axe du monde, [...] qui soutient l'âme du guerrier lors de ses combats." <br/><br/>Kim DANIERE</p>
