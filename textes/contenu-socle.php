		<h2>STèLE</h2>
			<p>Une stèle est un monument monolithe dressé, généralement plat et porteur d'inscriptions, symboles, gravures ou sculptures, de nature commémorative, funéraire, religieuse... servant le plus souvent à marquer l’emplacement d’une sépulture.</p>
