			<h2 id="sculpteur">Sculpt&eacute; par Ch. Richefeu</h2>
                        <p class="labeurpresa">"Le Chant du d&eacute;part" (&eacute;galement appel&eacute;e "La victoire en chantant") est un chant révolutionnaire et un hymne à la liberté contre toute forme de despotisme, &eacute;crit par <a href="http://fr.wikipedia.org/wiki/Étienne-Nicolas_Méhul">Étienne Nicolas Méhul</a> (pour la musique) et <a href="http://fr.wikipedia.org/wiki/Marie-Joseph_Chénier">Marie-Joseph Chénier</a> (pour les paroles) en 1794. Cet "Hymne" fut utilisé lors de la Première Guerre mondiale pour exalter les soldats partant au front lors de la mobilisation. Apr&egrave;s guerre, il fut r&eacute;utilis&eacute; gr&acirc;ce aux monuments aux morts.</p> 
 <p class="labeurpresa2"><span>Casque Adrian</span><br/>
C'est le casque militaire équipant les troupes françaises pendant la Première Guerre mondiale. Il fut conçu dans l'urgence quand des millions de soldats se retrouvèrent engagés dans la guerre de tranchées et que les blessures à la tête devinrent la cause d'une proportion significative des pertes sur le champ de bataille.</p>
 <p class="labeurpresa2"><span>Fusil Lebel</span><br/>
Arme symbolique de l'infanterie française pendant la Grande Guerre, il fut baptisé du nom d’un des membres de la commission qui a contribue à sa création : le colonel Nicolas Lebel. 
