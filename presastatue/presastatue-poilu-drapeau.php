			<h2 id="sculpteur">Sculpt&eacute; par Ch. Pourquet</h2>
                        <p class="labeurpresa">Cette statue repr&eacute;sente un poilu debout et brandissant dans son poing droit une palme et une couronne de laurier. Le soldat est moustachu et en uniforme complet, y compris la capote et le casque Adrian. Il tient un fusil Lebel dans sa main gauche ; une cartouchière est passée à sa ceinture et un masque à gaz pend en bandoulière. Il arbore la Légion d'honneur, la Médaille militaire, et la Croix de guerre.</p> 
<p class="labeurpresa2"><span>Casque Adrian</span><br/>
C'est le casque militaire équipant les troupes françaises pendant la Première Guerre mondiale. Il fut conçu dans l'urgence quand des millions de soldats se retrouvèrent engagés dans la guerre de tranchées et que les blessures à la tête devinrent la cause d'une proportion significative des pertes sur le champ de bataille.</p>                        
<p class="labeurpresa2"><span>Croix de guerre</span><br/>
C'&eacute;tait une décoration militaire attribuée &agrave; un soldat pour récompenser une conduite exceptionnelle au cours de guerre.</p>                        
